package com.github.adriens.open.data.fromages;

/**
 *
 * @author salad74
 */
public class Fromage {
    private String departement;
    private String fromage;
    private String page_francaise;
    private String english_page;
    private String image;
    private String lait;

    /**
     * @return the departement
     */
    public String getDepartement() {
        return departement;
    }

    /**
     * @param departement the departement to set
     */
    public void setDepartement(String departement) {
        this.departement = departement;
    }

    /**
     * @return the fromage
     */
    public String getFromage() {
        return fromage;
    }

    /**
     * @param fromage the fromage to set
     */
    public void setFromage(String fromage) {
        this.fromage = fromage;
    }

    /**
     * @return the page_francaise
     */
    public String getPage_francaise() {
        return page_francaise;
    }

    /**
     * @param page_francaise the page_francaise to set
     */
    public void setPage_francaise(String page_francaise) {
        this.page_francaise = page_francaise;
    }

    /**
     * @return the english_page
     */
    public String getEnglish_page() {
        return english_page;
    }

    /**
     * @param english_page the english_page to set
     */
    public void setEnglish_page(String english_page) {
        this.english_page = english_page;
    }

    /**
     * @return the image
     */
    public String getImage() {
        return image;
    }

    /**
     * @param image the image to set
     */
    public void setImage(String image) {
        this.image = image;
    }

    /**
     * @return the lait
     */
    public String getLait() {
        return lait;
    }

    /**
     * @param lait the lait to set
     */
    public void setLait(String lait) {
        this.lait = lait;
    }
    
}
